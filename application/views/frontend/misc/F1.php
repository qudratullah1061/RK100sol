                            <p>Your diet is at least half of your success in the gym, so a personal trainer will want to know how your eating habits look like. Again, be honest.</p>
                            <p>Last, but not least, the trainer will want to know about your day-to-day habits. How much time do you sleep, are you under a lot of stress, do you sit all day at work… those are all questions that will allow them to create the best possible training program for your situation.</p>

                            <h2 class="text-purple font-weight-700 text-normal margin-5px-top margin-5px-bottom">2. Determine your goals or “Where do you want to go?”</h2>
