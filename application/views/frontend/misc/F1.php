                            <h2 class="text-purple font-weight-700 text-normal margin-5px-top margin-5px-bottom">1. A full assessment or “Where are you at right now?”</h2>
                            <p>If you want to get somewhere, you first need to know where you’re starting from. </p>
                            <p>The first thing a good personal trainer will do is take a good long look at your current level of fitness and your current lifestyle habits.</p>
